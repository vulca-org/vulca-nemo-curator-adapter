def test_missing_pyarrow_has_actionable_extra_guidance(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    real_import = builtins.__import__

    def blocked_pyarrow_import(
        name: str,
        globals: dict[str, object] | None = None,
        locals: dict[str, object] | None = None,
        fromlist: tuple[str, ...] = (),
        level: int = 0,
    ) -> Any:
        if name == "pyarrow" or name.startswith("pyarrow."):
            raise ModuleNotFoundError("blocked for test", name="pyarrow")
        return real_import(name, globals, locals, fromlist, level)

    monkeypatch.setattr(builtins, "__import__", blocked_pyarrow_import)

    with pytest.raises(
        ValueError,
        match=r'fixture generation requires pip install "\.\[nemo-parquet\]"',
    ):
        build_fixtures(ROOT / "samples", tmp_path)
