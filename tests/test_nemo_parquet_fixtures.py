from __future__ import annotations

import builtins
import json
from pathlib import Path
from typing import Any

import pytest

from scripts.build_nemo_image_writer_parquet_fixtures import FIXTURES, build_fixtures
from vulca_curator_adapter.ingest.nemo_parquet import iter_nemo_parquet_signals

pa = pytest.importorskip("pyarrow")
pq = pytest.importorskip("pyarrow.parquet")


ROOT = Path(__file__).resolve().parents[1]
SOURCE_FIRST_COLUMNS = [
    "image_id",
    "tar_file",
    "member_name",
    "original_path",
    "metadata",
]
DOCUMENTED_ENRICHED_COLUMNS = SOURCE_FIRST_COLUMNS + [
    "aesthetic_score",
    "nsfw_score",
]


def _expected_schema(columns: list[str]) -> Any:
    return pa.schema(
        [
            pa.field(
                name,
                pa.float64()
                if name in {"aesthetic_score", "nsfw_score"}
                else pa.string(),
                nullable=False,
            )
            for name in columns
        ]
    )


def _source_rows(filename: str) -> list[dict[str, object]]:
    rows = json.loads((ROOT / "samples" / filename).read_text(encoding="utf-8"))
    assert isinstance(rows, list)
    return rows


def _signal_rows(path: Path) -> list[dict[str, Any]]:
    return [
        signal.model_dump(mode="json")
        for signal in iter_nemo_parquet_signals(path, batch_size=1)
    ]


def _copy_source_files(tmp_path: Path) -> Path:
    source_dir = tmp_path / "source"
    source_dir.mkdir()
    for source_name, _ in FIXTURES.values():
        (source_dir / source_name).write_text(
            (ROOT / "samples" / source_name).read_text(encoding="utf-8"),
            encoding="utf-8",
        )
    return source_dir


def _rewrite_source(
    source_dir: Path, fixture_name: str, rows: list[dict[str, object]]
) -> None:
    source_name = FIXTURES[fixture_name][0]
    (source_dir / source_name).write_text(
        json.dumps(rows, indent=2, allow_nan=True) + "\n",
        encoding="utf-8",
    )


def test_readable_sources_are_two_synthetic_rows_with_exact_column_order() -> None:
    source_first = _source_rows(FIXTURES["source_first"][0])
    documented_enriched = _source_rows(FIXTURES["documented_enriched"][0])

    assert len(source_first) == len(documented_enriched) == 2
    assert [list(row) for row in source_first] == [SOURCE_FIRST_COLUMNS] * 2
    assert [list(row) for row in documented_enriched] == [
        DOCUMENTED_ENRICHED_COLUMNS
    ] * 2
    assert [row["image_id"] for row in source_first] == [
        "source-first-001",
        "source-first-002",
    ]
    assert source_first[0]["metadata"] == (
        "{'source_url': 'https://example.com/nemo-source/source-first-001', "
        "'caption': 'Synthetic source-first candidate'}"
    )
    assert source_first[1]["metadata"] == "{}"
    serialized = json.dumps([source_first, documented_enriched])
    assert "".join(("/", "Users/")) not in serialized
    assert " ".join(("generated by", "NVIDIA")) not in serialized


def test_builds_exact_source_first_schema_rows_and_signals(tmp_path: Path) -> None:
    path = build_fixtures(ROOT / "samples", tmp_path)["source_first"]

    assert pq.ParquetFile(path).schema_arrow == _expected_schema(SOURCE_FIRST_COLUMNS)
    assert pq.read_table(path).to_pylist() == _source_rows(FIXTURES["source_first"][0])
    signals = list(iter_nemo_parquet_signals(path, batch_size=1))
    assert [signal.asset_id for signal in signals] == [
        "source-first-001",
        "source-first-002",
    ]
    assert [signal.aesthetic_score for signal in signals] == [None, None]
    assert [signal.nsfw_score for signal in signals] == [None, None]
    assert [signal.curator_metadata["parquet_provenance"] for signal in signals] == [
        {"sidecar": path.name, "row_number": 1},
        {"sidecar": path.name, "row_number": 2},
    ]


