def test_rollback_failure_is_explicit_and_chained_from_publication_failure(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    source_dir = _copy_source_files(tmp_path)
    out_dir = tmp_path / "out"
    out_dir.mkdir()
    for name, (_, output_name) in FIXTURES.items():
        (out_dir / output_name).write_bytes(f"sentinel-{name}".encode())

    real_replace = Path.replace
    second_output = FIXTURES["documented_enriched"][1]
    publication_error = OSError("synthetic second publish failure")
    rollback_error = OSError("synthetic rollback failure")
    publication_failed = False
    rollback_failed = False

    def fail_publication_then_rollback(self: Path, target: Path) -> Path:
        nonlocal publication_failed, rollback_failed
        if (
            not publication_failed
            and self.name == second_output
            and Path(target) == out_dir / second_output
        ):
            publication_failed = True
            raise publication_error
        if publication_failed and not rollback_failed:
            rollback_failed = True
            raise rollback_error
        return real_replace(self, target)

    monkeypatch.setattr(Path, "replace", fail_publication_then_rollback)

    with pytest.raises(
        RuntimeError,
        match=(
            "^fixture publication failed and rollback failed: "
            "publish error: synthetic second publish failure; "
            "rollback error: synthetic rollback failure$"
        ),
    ) as caught:
        build_fixtures(source_dir, out_dir)

    assert caught.value.__cause__ is publication_error
    assert {path.name for path in out_dir.iterdir()} == set(
        output_name for _, output_name in FIXTURES.values()
    )


